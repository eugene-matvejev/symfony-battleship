<?php

namespace AppBundle\Library\Traits;

trait Nameable {
    /**
     * @ORM\Column(name="amount", type="string", nullable=false)
     *
     * @var string
     */
    protected $name;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     *
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }
}